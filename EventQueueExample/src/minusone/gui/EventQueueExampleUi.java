  private void sound1Button(ActionEvent actionEvent)
  {
    SoundPlayingEvent sound = new SoundPlayingEvent(10, 100, "sound1.mp3");
    NewSoundEventNotifier newSoundEvent = new NewSoundEventNotifier(sound,
                                                                    parentEqe_);
    Thread notifierThread = new Thread(newSoundEvent, "PROUT");
    notifierThread.start();
  }
  
  
  
